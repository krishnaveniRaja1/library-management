<div id="wrapper"><p>A Library Management System (LMS) is a tool to help any libraries which are still using the old way to manage their library. The old way like searching for a book using manual work is hassle, fast report generation is not possible, information about issue/return of the books are not properly maintained, no central database can be created as information is not available in database. But by using the LMS, user can overcome all the problems mentioned above. This system can manage all the happenings of the library. Book transactions including book searching, availability of the book, details and appearance of the book, personal book borrowing history and etc. can be very easily handled by this system. This system is suitable for small to big libraries including medical and legal libraries, colleges, schools, universities, corporate houses and other academic resource centers. However, I would like to focus on LMS for colleges or universities.
LMS has three modules which are Students Module, Staff Module, and Administration Module. First of all, Students Module allows students to search material by title, name/author, subject, publication, series, ISBN/ISSN and etc. it also allow students to find recently arrived material. In Staff Module, staff can issue book and receive book with bar code. Staff also can fine for books received after due date. Lastly, the administration module help administrator to register/reassign shelf and category, change book status, issue library card, configuration to register users, and database backup/restore. Overall this system can be very helpful and it can make things easier.</p>
